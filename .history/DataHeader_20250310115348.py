def group_by_prefix(WC):
    grouped_list = []
    prefix = None
    current_group = []

    for item in WC:
        # Extract the prefix (e.g., 'BC30') from the first part of the string
        current_prefix = item.split('_')[0]

        if prefix is None or prefix != current_prefix:
            # If the prefix is different or hasn't been set yet, start a new group
            if current_group:
                # Add the current group to the grouped list before starting a new group
                grouped_list.append(current_group)
            prefix = current_prefix
            current_group = []

        current_group.append(item)

    # Add the last group to the grouped list
    if current_group:
        grouped_list.append(current_group)

    return grouped_list


